package com.example.zfrproject.cardViewSupport;

public class Introduction {
    private String name;

    private int imageId;

    public Introduction(String name, int imageId) {
        this.name = name;
        this.imageId = imageId;
    }

    public String getName() {
        return name;
    }

    public int getImageId() {
        return imageId;
    }
}
